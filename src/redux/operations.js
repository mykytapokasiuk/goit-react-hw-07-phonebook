import axios from 'axios';
import { createAsyncThunk } from '@reduxjs/toolkit';

axios.defaults.baseURL = 'https://64cac682700d50e3c7054934.mockapi.io/api/';

export const fetchContactsThunk = () =>
  createAsyncThunk('contacts/fetchAll', async (_, thunkApi) => {
    try {
      const response = await axios.get('/contacts');
      return response.data;
    } catch (error) {
      return thunkApi.rejectWithValue(error.message);
    }
  });

export const addContactThunk = () =>
  createAsyncThunk('contacts/addContact', async (contact, thunkApi) => {
    try {
      const response = await axios.post('/contacts', contact);
      return response.data;
    } catch (error) {
      return thunkApi.rejectWithValue(error.message);
    }
  });

export const deleteContactThunk = () =>
  createAsyncThunk('contacts/deleteContact', async (contactId, thunkApi) => {
    try {
      const response = await axios.delete(`/contacts/${contactId}`);
      return response.data;
    } catch (error) {
      return thunkApi.rejectWithValue(error.message);
    }
  });
